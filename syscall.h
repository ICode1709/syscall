extern unsigned long long syscall_data[8][2];
extern const short syscall_table[SYSCALLID_MAX][WINVER_MAX];
extern const short system_builds[WINVER_MAX];

__forceinline short GetSyscallNumber(const short id, short build)
{
    short syscall = 0;

    for (short winver = WINVER_MAX; winver > 0; --winver)
        syscall += (system_builds[winver - 1] > build) ? 0 : syscall_table[id][winver - 1];

    return syscall;
}
__forceinline bool IsBuildSupported(short build)
{
    for (int i = 0; i < WINVER_MAX; ++i)
        if (system_builds[i] == build) return true;
    return false;
}

template <typename ret = NTSTATUS, typename... args> __forceinline ret syscall(unsigned long index, args... arguments)
{
    unsigned long long* data = syscall_data[(((unsigned int)(1406958263 - 1029531031 * __rdtsc()) >> 15) & 7)];

#if _WIN64
	switch (sizeof...(arguments))
	{
	case 0:
		/*
		mov eax, ecx
		syscall
		ret
		*/
		data[0] = 0xC3050FC18B;
		break;
	case 1:
		/*
		mov r10, rcx
		mov eax, edx
		syscall
		ret
		*/
		data[0] = 0xC3050FC28BD18B4C;
		break;
	case 2:
		/*
		mov r10, rcx
		mov eax, r8d
		syscall
		ret
		*/
		data[0] = 0x50FC08B41D18B4C;
		data[1] = 0xC3;
		break;
	case 3:
		/*
		mov r10, rcx
		mov eax, r9d
		syscall
		ret
		*/
		data[0] = 0x50FC18B41D18B4C;
		data[1] = 0xC3;
		break;
	default:
		/*
		mov r10, rcx
		mov eax, [rsp + 8 + argcount * 8]
		syscall
		ret
		*/
		data[0] = MAKELONGLONG(0x8BD18B4C, MAKELONG(0x2484, 8 + sizeof...(arguments) * 8));
		data[1] = 0xC3050F0000;
		break;
	}
#else
    /*
    mov eax, index
    call dword ptr [ntdll!_Wow64Transition]
    ret 000C
    */

    /*
    2D140000 - B8 36000000           - mov eax,index
    2D140005 - E8 03000000           - call 2D14000D
    2D14000A - C2 1000               - ret 0010
    2D14000D - EA 1600142D 3300      - jmp 0033:2D140016
    2D140014 - 00 00                 - add [rax],al
    2D140016 - FF C1                 - inc ecx
    2D140018 - FF A7 F8000000        - jmp qword ptr [rdi+000000F8]
    */

    /*
    mov eax, STATUS_NOT_SUPPORTED
    ret
    */
    data[0] = 0xC3C00000BBB8;
#endif
	NTSTATUS status = reinterpret_cast<ret(*)(args..., unsigned long)>(data)(arguments..., index);
	data[0] = 0x0;
	data[1] = 0x0;
	return status;
}

